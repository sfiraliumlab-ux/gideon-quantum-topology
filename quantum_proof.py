def build_macroscopic_entanglement(sai_index):
    """
    Генерация квантовой цепи для проверки гипотезы семинара.
    Доказательство: макроскопическая голограмма (запутанность) сохраняется 
    при обнулении энергетического баланса (энтропии).
    """
    if sai_index < 0.99:
        return "// ОШИБКА: Система не достигла S-перехода. Запутанность невозможна."

    qasm = [
        "OPENQASM 2.0;",
        "include \"qelib1.inc\";",
        "qreg q[4]; // Кубиты макро-объектов (Земля-Луна)",
        "creg c[4];",
        "",
        "// Инициализация S-перехода (Нулевая энтропия)",
        "h q[0];",
        "h q[2];",
        "",
        "// Информационное всеединство (Мгновенный обмен)",
        "cx q[0], q[1];",
        "cx q[2], q[3];",
        "cz q[1], q[2]; // Топологический мост",
        "",
        "// Измерение сохранения связности",
        "measure q[0] -> c[0];",
        "measure q[1] -> c[1];",
        "measure q[2] -> c[2];",
        "measure q[3] -> c[3];"
    ]
    return "\n".join(qasm)

if __name__ == "__main__":
    print(build_macroscopic_entanglement(1.0))
